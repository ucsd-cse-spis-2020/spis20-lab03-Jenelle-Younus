"""" 
draws the letter J using Turtle 
"""

import turtle

def drawJ(theTurtle):
  # top horizontal part of J
  theTurtle.forward(100)
  theTurtle.backward(50)
  # middle vertical part of J
  theTurtle.right(90)
  theTurtle.forward(100)
  # curved part of J
  theTurtle.right(90)
  theTurtle.forward(50)
  theTurtle.right(90)
  theTurtle.forward(25)

turtle1 = turtle.Turtle()
turtle2 = turtle.Turtle()
drawJ(turtle1)
drawJ(turtle2)
